    //TELA PARA O ELEITOR VOTAR
    private void votarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_votarButtonActionPerformed

        bVota = true;
        //INCLUI VOTAÇÃO
        if (nomeEleitor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Eleitor!!!!\n Por favor informe seu nome.", "Votação", JOptionPane.WARNING_MESSAGE);
            bVota = false;
            return;
        } else {

            if (numeroTitulo.getText().equals("")) {
                JOptionPane.showMessageDialog(null, nomeEleitor.getText() + ", por favor informe o seu número do título de eleitor", "Votação", JOptionPane.WARNING_MESSAGE);
                bVota = false;
                return;
            }
        }

        if (pesquisanumerocandidato.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o número do seu candidato.", "Votação", JOptionPane.WARNING_MESSAGE);
            bVota = false;
            return;
        }

        if (bVota) {

            if (!bVotoPrefeito) {

                //CHAMA METODO PARA ADICIONAR VOTO PARA O CANDIDATO
                urna.VotaCandidato(pesquisanumerocandidato.getText());

                bVotoPrefeito = true;
                JOptionPane.showMessageDialog(null, "Por favor, vote no seu candidato a Prefeito", "Votação", JOptionPane.WARNING_MESSAGE);
                pesquisanumerocandidato.setText("");
                nomeCandidatoLabel.setText("");
                partidoCandidatoLabel.setText("");
                tipoCandidatoLabel.setText("");
                pesquisanumerocandidato.enable(true);

            } else {

                //CHAMA METODO PARA ADICIONAR VOTO PARA O CANDIDATO
                urna.VotaCandidato(pesquisanumerocandidato.getText());

                bVotoPrefeito = false;
                JOptionPane.showMessageDialog(null, "Votação concluida e contabilizada. Obrigado", "Votação", JOptionPane.WARNING_MESSAGE);
                LimpaCamposVotacao();
                SetVisible("FinalizaVotacao");
            }
        }
    }//GEN-LAST:event_votarButtonActionPerformed

    //BOTÃO PARA EFETUAR O CANCELAMENTO DA VOTAÇÃO E VOLTAR PARA TELA INICIAL
