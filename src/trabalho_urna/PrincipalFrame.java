    //METODO PARA CENTRALIZAR OS PANELS NO CENTRO DO JFRAME
    public void centralizarContainer(JPanel filho) {
        // obter dimensões do pai
        int larguraPai = this.getWidth();
        int alturaPai = this.getHeight();
        // obter dimensões do filho
        int larguraFilho = filho.getWidth();
        int alturaFilho = filho.getWidth();
        // calcular novas coordenadas do filho  
        int novoX = (larguraPai - larguraFilho);
        int novoY = (alturaPai - alturaFilho);
        // centralizar filho
        filho.getParent().setLayout(new GridBagLayout());
        filho.setSize(new Dimension(novoX, novoY));
        filho.repaint();
    }

    //METODO PARA OCULTAR E MOSTRAR OS PANELS DE ACORDO COM OS BOTOES SELECIONADOSS.
    private void SetVisible(String strTela) {
        switch (strTela) {
            case "TelaInicial":
            case "CancelarTelaCadastro":
            case "CancelarVotacao":
            case "FinalizaVotacao":
            case "BotaoVoltarResultadoVotacao":
                inicialPanel.setVisible(true);
                cadastroPanel.setVisible(false);
                votacaoPanel.setVisible(false);
                resultadoVotacaoPanel.setVisible(false);
                break;

            case "BotaoCadastro":
                cadastroPanel.setVisible(true);
                inicialPanel.setVisible(false);
                votacaoPanel.setVisible(false);
                break;

            case "IniciarVotacao":
                votacaoPanel.setVisible(true);
                inicialPanel.setVisible(false);
                cadastroPanel.setVisible(false);
                break;

            case "ResultadoVotacao":
                resultadoVotacaoPanel.setVisible(true);
                inicialPanel.setVisible(false);
                cadastroPanel.setVisible(false);
                votacaoPanel.setVisible(false);
                break;
        }
    }

