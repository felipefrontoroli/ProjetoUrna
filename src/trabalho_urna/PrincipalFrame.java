    //BOTAO PARA ENCERRAR VOTAÇÃO
    private void encerrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encerrarButtonActionPerformed

        SetVisible("ResultadoVotacao");

        Object[][] candidatosMatriz;
        String[] colunas = {"Nome Candidato", "Número Candidato", "Partido", "Tipo Candidato", "Total votos"};

        List<Candidato> _CandidatoList = urna.ResultadoEleicao("Prefeito");
        _CandidatoList.addAll(urna.ResultadoEleicao("Vereador"));
        
        candidatosMatriz = new Object[_CandidatoList.size()][5];
        for (int i = 0; i < _CandidatoList.size(); i++) {
            for (int j = 0; j < 5; j++) {
                switch (j) {
                    case 0:
                        candidatosMatriz[i][j] = _CandidatoList.get(i).getNomeCandidato();
                        break;

                    case 1:
                        candidatosMatriz[i][j] = _CandidatoList.get(i).getNumeroCandidato();
                        break;

                    case 2:
                        candidatosMatriz[i][j] = _CandidatoList.get(i).getPartidoCandidato();
                        break;

                    case 3:
                        candidatosMatriz[i][j] = _CandidatoList.get(i).getTipoCandidato();
                        break;

                    case 4:
                        candidatosMatriz[i][j] = Integer.toString(_CandidatoList.get(i).getNumeroVotos());
                        break;
                }
            }
        }

        JTable candidatosTable = new JTable(candidatosMatriz, colunas);
        candidatosTable.setMinimumSize(new Dimension(600,200));
        JTableHeader header = candidatosTable.getTableHeader();
        JScrollPane barra = new JScrollPane(candidatosTable);
        barra.setMinimumSize(new Dimension(600, 200));
        
        candidatosTable.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        getContentPane().add(barra);
        
        
    }//GEN-LAST:event_encerrarButtonActionPerformed

