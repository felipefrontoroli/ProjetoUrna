    //SALVA CANDIDATO NA LISTA
    private void salvarCandidatoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCandidatoButtonActionPerformed

        bCadastra = true;

        if (nomeCandidatoFormattedTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o nome do candidato.", "Candidato", JOptionPane.WARNING_MESSAGE);
            bCadastra = false;
            return;
        }

        if (partidoFormattedTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o partido do candidato.", "Candidato", JOptionPane.WARNING_MESSAGE);
            bCadastra = false;
            return;
        }

        if (numeroCandidatoFormattedTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o número do candidato.", "Candidato", JOptionPane.WARNING_MESSAGE);
            bCadastra = false;
            return;
        }

        //VERIFICA SE NUMERO JA EXISTE
        if (urna.VerificaNumeroCandidato(numeroCandidatoFormattedTextField.getText())) {
            JOptionPane.showMessageDialog(null, "Número do Candidato já cadastrado.", "Candidato", JOptionPane.WARNING_MESSAGE);
            bCadastra = false;
            return;
        }

        if (bCadastra) {

            urna.CadastroCandidatos(new Candidato(nomeCandidatoFormattedTextField.getText(),
                    partidoFormattedTextField.getText(),
                    numeroCandidatoFormattedTextField.getText(),
                    cargoCandidatoComboBox.getSelectedItem().toString()));

            nomeCandidatoFormattedTextField.setText("");
            partidoFormattedTextField.setText("");
            numeroCandidatoFormattedTextField.setText("");
            cargoCandidatoComboBox.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "Candidato cadastrado com sucesso.", "Candidato", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_salvarCandidatoButtonActionPerformed

