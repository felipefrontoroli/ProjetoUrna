    //EVENTO CHAMADO QUANDO O FOCO SAI DO CAMPO DE PESQUISA CANDIDATO PELO NUMERO
    //AO SAIR O FOCO DO CAMPO É CHAMADO O METODO DA CLASSE URNA PARA PESQUISAR O CANDIDATO E RETORNAR OS DADOS DELE.
    private void pesquisanumerocandidatoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pesquisanumerocandidatoFocusLost

        if (!pesquisanumerocandidato.getText().equals("")) {
            Candidato objCandidato = new Candidato();
            objCandidato = urna.RetornaCandidato(pesquisanumerocandidato.getText(), (bVotoPrefeito == true ? "Prefeito" : "Vereador"));

            if (objCandidato != null) {
                nomeCandidatoLabel.setText(objCandidato.getNomeCandidato());
                partidoCandidatoLabel.setText(objCandidato.getPartidoCandidato());
                tipoCandidatoLabel.setText(objCandidato.getTipoCandidato());
                pesquisanumerocandidato.enable(false);
            } else {
                nomeCandidatoLabel.setText("");
                partidoCandidatoLabel.setText("");
                pesquisanumerocandidato.setText("");
                tipoCandidatoLabel.setText("");
                pesquisanumerocandidato.enable(true);

                JOptionPane.showMessageDialog(null, "Candidato não cadastrado. Por favor, informe outro candidato", "Candidato", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_pesquisanumerocandidatoFocusLost

